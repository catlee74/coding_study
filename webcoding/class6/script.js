//연산과 연산자

//산술연산
console.log(3 + 5);
console.log(3 - 5);
console.log(3 * 5);
console.log(3 / 5);
console.log(3 % 5);

//대입연산
let result = 1 + 1;
console.log(result);

result = 1 - 1;
console.log(result);

result = 1 * 1;
console.log(result);

result = 1 / 2;
console.log(result);

//복합 대입 연산자
